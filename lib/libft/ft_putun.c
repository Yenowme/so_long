#include "libft.h"

void	ft_putun(unsigned int n, int *rt)
{
	char	a;

	if (n < 10)
	{
		a = n % 10 + '0';
		*rt += write(1, &a, 1);
	}
	else
	{
		ft_putun(n / 10, rt);
		a = n % 10 + '0';
		*rt += write(1, &a, 1);
	}
}
